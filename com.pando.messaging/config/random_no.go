package config

import (
	cryptorand "crypto/rand"
	"io"
)

/********************************************************Random no.**************************************/
func RandomNumber(max int) string {
	b := make([]byte, max)
	n, err := io.ReadAtLeast(cryptorand.Reader, b, max)
	if n != max {
		panic(err)
	}
	for i := 0; i < len(b); i++ {
		b[i] = table[int(b[i])%len(table)]
	}
	return string(b)
}

